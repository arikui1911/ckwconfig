    def self.validate(color)
      return true if COLORS.key?(color)
      msg = begin
              if color.between?(0, 0xffffff)
                nil
              else
                "color out of range: #{color.inspect} (for 000000 .. FFFFFF)"
              end
            rescue ArgumentError, NoMethodError
              "invalid color: #{color.inspect}"
            end
      raise ArgumentError, msg if msg
      true
    end

